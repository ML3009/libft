#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t			i;
	unsigned char	*str;

	if (!n)
		return (NULL);
	str = (unsigned char *)s;
	i = 0;
	if ((unsigned char)c == '\0')
		return (&str[i]);
	while (i < n)
	{
		if (str[i] == (unsigned char)c)
			return (&str[i]);
		i++;
	}
	return (NULL);
}
